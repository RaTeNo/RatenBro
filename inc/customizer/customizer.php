function raten_customizer_body_classes( $classes ) {

    /********************************************************************
     * Sidebar
     */

    // Сайдбар на главной
    if ( raten_get_option( 'structure_home_sidebar' ) == 'none' && is_front_page() ) {
        $sidebar_class = 'sidebar-none';
    }
    if ( raten_get_option( 'structure_home_sidebar' ) == 'left' && is_front_page() ) {
        $sidebar_class = 'sidebar-left';
    }

    // Сайдбар в архивах
    if ( raten_get_option( 'structure_archive_sidebar' ) == 'none' && is_archive() ) {
        $sidebar_class = 'sidebar-none';
    }
    if ( raten_get_option( 'structure_archive_sidebar' ) == 'left' && is_archive() ) {
        $sidebar_class = 'sidebar-left';
    }

    // Сайдбар записи
    if ( raten_get_option( 'structure_single_sidebar' ) == 'none' && is_single() ) {
        $sidebar_class = 'sidebar-none';
    }
    if ( raten_get_option( 'structure_single_sidebar' ) == 'left' && is_single() ) {
        $sidebar_class = 'sidebar-left';
    }

    // Сайдбар страниц
    if ( raten_get_option( 'structure_page_sidebar' ) == 'none' && is_page() ) {
        $sidebar_class = 'sidebar-none';
    }
    if ( raten_get_option( 'structure_page_sidebar' ) == 'left' && is_page() ) {
        $sidebar_class = 'sidebar-left';
    }

    // настройки для отдельной статьи
    if ( is_single() || is_page() ) {
        global $post;
        if ( 'checked' == get_post_meta( $post->ID, 'sidebar_hide', true ) ) {
            $sidebar_class = 'sidebar-none';
        }
    }

    $classes[] = $sidebar_class;


    /********************************************************************
     * Skin
     */
    $skin = raten_get_option( 'skin' );
    if ( ! empty( $skin ) && $skin != 'no' ) {
        $classes[] = $skin;
    }


    return $classes;
}
add_filter( 'body_class', 'raten_customizer_body_classes' );




/**
